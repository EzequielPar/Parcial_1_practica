personas = 0

while personas <= 5:
    nombre = input("Ingresa tu nombre: ")
    apellido = input("Ingresa tu apellido: ")
    edad = int(input("Ingresa tu edad: "))

    print("Nombre:", nombre)
    print("Apellido:", apellido)
    print("Edad:", edad)

    personas += 1

print("Fin del programa")

